app.get("/winners", async (req, res) => {
  const results = [];

  const raffleContract = new ethers.Contract(
    contractAddress,
    raffleAbi,
    provider
  );
  try {
    const count = await raffleContract.counter();
    for (let i = 0; i < parseInt(count); i++) {
      const transaction = await raffleContract.raffle(i);
      if (!transaction.raffleActive) {
        const metadata = await alchemy.nft.getNftMetadata(
          transaction.nftAddress,
          transaction.tokenId
        );
        const winner = await raffleContract.winners(transaction.raffleId);
        console.log("raffle winner", winner);
        results.push({ transaction, metadata, winner });
      }
    }
    console.log(results);
  } catch (error) {
    console.log(error);
  }
  res.json(results);
});

