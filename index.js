app.get("/counter", async (req, res) => {
  const results = [];

  const raffleContract = new ethers.Contract(
    contractAddress,
    raffleAbi,
    provider
  );
  try {
    console.log("fetching Data");
    const count = await raffleContract.counter();
    for (let i = 0; i < parseInt(count); i++) {
      const transaction = await raffleContract.raffle(i);
      if (transaction.raffleActive) {
        const metadata = await alchemy.nft.getNftMetadata(
          transaction.nftAddress,
          transaction.tokenId
        );
        results.push({ transaction, metadata });
      }
    }
  } catch (error) {
    console.log(error);
  }
  res.json(results);
});

